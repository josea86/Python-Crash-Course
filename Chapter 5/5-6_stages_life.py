age = 80

if age < 2:
    print ("It's a baby")
elif age == 2 or age < 4:
    print ("It's a toddler")
elif age == 4 or age < 13:
    print ("It's a kid")
elif age == 13 or age < 20:
    print ("It's a teenager")
elif age == 20 or age < 65:
    print ("It's an adult")
elif age >= 65:
    print ("It's an elder")